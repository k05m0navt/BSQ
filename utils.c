#include <unistd.h>
#include <fcntl.h>
#include <stdlib.h>
#include "ft_bsq.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		ft_putchar(str[i++]);
}

int		ft_atoi(char *str)
{
	int		start;
	int		i;
	int		res;
	int		negative;

	if (!str || !*str)
		return (0);
	start = 0;
	while (str[start] != '\0' && (str[start] == ' ' || str[start] == '\t' ||
			str[start] == '\r' || str[start] == '\n'))
		start++;
	i = start;
	res = 0;
	negative = 0;
	while (str[i] != '\0' && ((str[i] >= '0' && str[i] <= '9') ||
				(i == start && (str[i] == '-' || str[i] == '+'))))
	{
		if (str[i] == '-')
			negative = 1;
		if (str[i] >= '0' && str[i] <= '9')
			res = (res * 10) + (str[i] - 48);
		++i;
	}
	return ((negative) ? res * -1 : res);
}

int		ft_min(int a, int b, int c)
{
	int		m;

	m = a;
	if (m > b)
		m = b;
	if (m > c)
		m = c;
	return (m);
}

int		ft_before_n(int fd)
{
	int		counter;
	char	buf[1];

	counter = 0;
	read(fd, buf, sizeof(buf));
	while (buf[0] != '\n')
	{
		counter++;
		read(fd, buf, sizeof(buf));
	}
	return (counter + 1);
}
